public class MathProblems {
    // Method to calculate the area of a rectangle
    public static double calculateRectangleArea(double length, double width) {
        return length * width;
    }

    // Method to add two numbers and round the result to 2 decimal places
    public static double addAndRoundNumbers(double num1, double num2) {
        double roundedResult = Math.round(num1 + num2);
        return roundedResult;
    }
}
