public class Main {
    public static void main(String[] args) {
        // Example code to solve mathematical problem
        int result = solveMathProblem(5, 3, 2);
        System.out.println("The result is: " + result);
    }

    private static int solveMathProblem(int a, int b, int c) {
        return (a * b) - c;
    }
}
